// Postfix (most common)
let x = 3;
let y = x++

console.log(`x:${x}, y:${y}`);
// This will output: x:3, y:4;

// Prefix
let a = 3;
let b = ++a;

console.log(`a:${a}, b:${b}`);
// This will output: a:4, b:4

